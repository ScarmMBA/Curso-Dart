/**
 * Un operador es un símbolo que le dice al compilador que
 * debe realizar una tarea matemática, relacional o lógica y
 * debe de producir un resultado
 */
main() {
int a = 10 + 5;     // +    15
a = 20 - 10;        // -    10
a= 10 * 2;          //*       20
double b = 10 /2;   //  /    5

b = 10.0 % 3;       //   %   residuo de la division
b = -b;             // -expresion   Es usado para cambiar el valor de la expresión
